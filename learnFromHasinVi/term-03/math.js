var x;
var y = 4;

x = y++

console.log("x = " + x + ", y = " + y);

/*
1. First Application

Explanation : x = y++
means first
x = y (that is 4)
than
y = y+1 (that is 5)
*/
var m;
var n = 4;

m = ++n

console.log("m = " + m + ", n = " + n);

/*
1. Secont Application

Explanation : m = ++n
means first
n = n + 1 (that is 5)
than
m = n (that is also 5)
*/